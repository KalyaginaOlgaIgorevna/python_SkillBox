char, n = input().split(',') # Считываем символ и число, разделенные запятой
n = int(n) # Преобразуем число в целое
# Вычисляем новую позицию символа, учитывая цикличность алфавита
new_char = chr((ord(char) - ord('a') + n) % 26 + ord('a'))
print(new_char) # Выводим новый символ

