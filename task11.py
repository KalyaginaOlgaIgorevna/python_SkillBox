#Если вы введите "1 2 3 4 5", то программа выведет False, так как в этой последовательности нет дубликатов.

#Если вы введите "1 2 3 4 1", то программа выведет True, так как в этой последовательности есть дубликаты числа 1.

#Если вы введите "42 42 42 42 42", то программа также выведет True, так как в этой последовательности все числа одинаковы и есть дубликаты.
numbers = input().split() # Считываем последовательность чисел, разделенных пробелами
has_duplicates = len(set(numbers)) != len(numbers) # Проверяем наличие дубликатов
print(has_duplicates) # Выводим результат в формате Boolean
