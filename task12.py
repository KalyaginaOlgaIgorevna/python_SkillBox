#Если вы введите "123-456-7890", то программа выведет "1234567890", так как символы "-" удалены.

#Если вы введите "+1 (800) 555-1234", то программа выведет "+18005551234", так как удалены все символы, кроме цифр и "+".
phone_number = input() # Считываем номер телефона
cleaned_number = ''.join(char for char in phone_number if char.isdigit() or char == '+') # Удаляем лишние символы
print(cleaned_number) # Выводим очищенный номер
