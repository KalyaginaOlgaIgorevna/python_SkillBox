#Если вы введите "123456789012", то программа выведет 'no', так как сумма цифр (1+3+5+7+9+1+2) делится на 10 без остатка, что делает штрих-код валидным.

#Если вы введите "123456789013", то программа выведет 'no', так как сумма 
barcode = input() # Считываем штрих-код
odd_sum = sum(int(barcode[i]) for i in range(0, len(barcode), 2)) # Сумма цифр на нечетных позициях
even_sum = sum(int(barcode[i]) for i in range(1, len(barcode), 2)) # Сумма цифр на четных позициях
is_valid = (odd_sum + 3 * even_sum) % 10 == 0 # Проверяем условие деления на 10
print('yes' if is_valid else 'no') # Выводим результат
