#Если вы введете "5", то программа выведет 20.00 25.00 7.07
import math

# Считываем длину стороны квадрата
side = float(input())

# Рассчитываем периметр квадрата
perimeter = 4 * side

# Рассчитываем площадь квадрата
area = side ** 2

# Рассчитываем длину диагонали квадрата
diagonal = side * math.sqrt(2)

# Выводим результаты, округленные до двух знаков после запятой
print(f"{perimeter:.2f} {area:.2f} {diagonal:.2f}")
